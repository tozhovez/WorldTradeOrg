    CONSTRAINT "wto_timeseries_ProductOrSectorCode_fkey" FOREIGN KEY ("ProductOrSectorCode") REFERENCES services("ProductOrSectorCode") ON UPDATE CASCADE ON DELETE CASCADE NOT DEFERRABLE
